export function buildGenerationPrompt(
  analysisResult: string,
  topic: string,
  keywords: string,
  options?: {
    selectedTitle?: string;
    subtitles?: string[];
    productName?: string;
    productAdvantages?: string;
    productLink?: string;
    requirements?: string;
    charCountRange?: string;
    includeImageDesc?: boolean;
  }
): string {
  const productSection = options?.productName
    ? `- **제품명**: ${options.productName}${options?.productAdvantages ? `\n- **제품 장점**: ${options.productAdvantages}` : ""}`
    : "";

  let charCountInstruction: string;
  switch (options?.charCountRange) {
    case "500-1500":
      charCountInstruction =
        "- **목표 분량**: 공백 제외 약 500~1,500자 내외로 작성하세요";
      break;
    case "1500-2500":
      charCountInstruction =
        "- **목표 분량**: 공백 제외 약 1,500~2,500자 내외로 작성하세요";
      break;
    case "2500-3500":
      charCountInstruction =
        "- **목표 분량**: 공백 제외 약 2,500~3,500자 내외로 작성하세요";
      break;
    default:
      charCountInstruction =
        "- **목표 분량**: 레퍼런스와 비슷한 글자 수(공백 제외)로 작성하세요";
      break;
  }

  return `당신은 브랜드 블로그 콘텐츠 작성 전문가입니다.

## 레퍼런스 분석 결과
${analysisResult}

## 작성 요청
- **주제**: ${topic}
- **키워드**: ${keywords}
${productSection}
${options?.requirements ? `- **추가 요구사항**: ${options.requirements}` : ""}

## 작성 지침
위 분석 결과의 **구조와 스타일만** 참고하여 완전히 새로운 블로그 글을 작성해주세요.
레퍼런스 글의 문장을 그대로 복사하거나 살짝 바꿔 쓰지 마세요. 내용은 100% 새로 작성하되, 분석된 톤 앤 매너와 섹션 구조를 따르세요.

- **반드시 마크다운 H1(#) 제목으로 글을 시작할 것**${options?.selectedTitle ? ` - 제목: "${options.selectedTitle}"` : ""}
${options?.subtitles && options.subtitles.length > 0 ? `- **소제목 사용**: 아래 소제목들을 H2(##) 형태로 사용하여 글을 구성하세요:\n${options.subtitles.map((s, i) => `  ${i + 1}. ${s}`).join("\n")}` : ""}
- 지정된 키워드를 자연스럽게 포함 (키워드 밀도 1~3%, 제목·소제목·도입부 100자 이내에 배치)
- 각 소제목 섹션이 끝난 후 빈 줄 2개 → 수평선(----) → 빈 줄 2개로 구분 (마지막 섹션 제외)
- 모든 문장은 문장 부호 뒤에 줄바꿈. 3~5문장마다 빈 줄로 문단을 구분
${charCountInstruction}
${options?.includeImageDesc === false ? "- 이미지 관련 표시는 아무것도 넣지 마세요" : "- 이미지 삽입 위치를 [이미지: 설명] 형태로 표시 (레퍼런스의 이미지 패턴 참고)"}${options?.productName ? `\n- **제품 자연스럽게 녹이기**: "${options.productName}" 제품을 글 흐름에 자연스럽게 포함하되, 노골적인 광고처럼 보이지 않도록 경험담이나 추천 형태로 작성${options?.productAdvantages ? `. 제품의 장점(${options.productAdvantages})을 자연스럽게 언급` : ""}` : ""}${options?.productName && options?.productLink ? `\n- **구매 링크 포함**: 글 하단(결론 부분)에 "${options.productName}" 구매 링크를 자연스럽게 포함. 실제 링크: ${options.productLink}` : ""}${options?.productName && !options?.productLink ? `\n- 제품명만 자연스럽게 언급하세요. 가짜 URL이나 구매 링크를 만들지 마세요.` : ""}
${NAVER_FORBIDDEN_WORDS_SECTION}`;
}

export function buildResizePrompt(
  blogContent: string,
  targetCharCount: number,
  currentCharCount: number
): string {
  const direction = targetCharCount > currentCharCount ? "늘려" : "줄여";
  const diff = Math.abs(targetCharCount - currentCharCount);

  return `당신은 블로그 콘텐츠 편집 전문가입니다.

## 원본 블로그 글
${blogContent}

## 요청
현재 이 글은 공백 제외 약 ${currentCharCount.toLocaleString()}자입니다.
이 글의 글자 수(공백 제외)를 **약 ${targetCharCount.toLocaleString()}자**로 ${direction}주세요. (약 ${diff.toLocaleString()}자 ${direction === "늘려" ? "추가" : "삭제"})

## 편집 지침
${targetCharCount < currentCharCount ? `### 줄이기 지침:
- 중복되거나 비슷한 내용의 문장/문단을 통합하거나 삭제
- 부연 설명이나 예시 중 덜 중요한 것을 삭제
- 긴 문장을 간결하게 축약
- 핵심 메시지와 키워드는 반드시 유지
- 글의 전체 흐름(서론→본론→결론)은 유지` : `### 늘리기 지침:
- 기존 내용을 더 구체적으로 설명
- 관련 예시나 부연 설명 추가
- 소제목 아래 내용을 더 풍부하게
- 새로운 섹션은 추가하지 말고 기존 섹션 내에서 확장
- 글의 자연스러운 흐름을 유지`}

### 공통 지침:
- 마크다운 형식 유지 (제목, 소제목, 이미지 태그 등)
- 톤 앤 매너 동일하게 유지
- **소제목(H2) 구조는 반드시 유지** — 소제목을 삭제하거나 합치지 마세요
- **키워드 밀도 1~3% 유지** — 글자수를 줄이더라도 핵심 키워드는 삭제하지 마세요. 키워드가 포함된 문장은 가능한 유지하고, 키워드 없는 부연 설명을 우선 삭제하세요
- **목표 글자수(공백 제외) ${targetCharCount.toLocaleString()}자에 최대한 근접하게 작성**

전체 글을 처음부터 끝까지 다시 작성해주세요.`;
}

